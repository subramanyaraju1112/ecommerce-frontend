import { Star } from "lucide-react"

type StarRatingProps = {
  rating: number
}

const StarRating = ({ rating }: StarRatingProps) => {
  return (
    <div className="flex gap-0.5">
      {[...Array(5)].map((_, i) => (
        <Star
          key={i}
          className={`w-3.5 h-3.5 ${
            i < rating
              ? "fill-yellow-400 text-yellow-400"
              : "text-gray-300"
          }`}
        />
      ))}
    </div>
  )
}

export default StarRating
